package pakage09;

public class ArrayExample {

	public static void main(String[] args) {
		int[] array= {1,2,3};
		int array1[]= {4,5,6};
		
		//for each loop
		for(int num : array) {
			System.out.print(num+" ");
		}
		System.out.println();
		for(int num : array1) {
			System.out.print(num+" ");
		}
	}

}
